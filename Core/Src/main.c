/**
  * @brief  Function implementing the mainTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartmainTask */
void StartmainTask(void *argument)
{
  /* USER CODE BEGIN 5 */
	int32_t ret = 0;
	
	txBuffer[0] = 0x30;
	for(int i = 0; i < BUFFERSIZE - 1; i++)
	{
		if(txBuffer[i] != 0x39)
			txBuffer[i+1] = txBuffer[i] + 1;
		else
			txBuffer[i+1] = 0x30;
	}
	
  /* Infinite loop */
  for(;;)
  {
		ret = socket(CLIENT_SOCKET, Sn_MR_TCP, localPort++, SF_TCP_NODELAY);
		if(localPort == 0xFFFF) {
			localPort = 50000;
		}
		
		if (ret < 0) {
			printf("socket failed{%d}.\n", ret);
			close(CLIENT_SOCKET);
			osDelay(500);
			continue;
		}
		else {
			printf("socket success...\n");
		}
		
		while(getSn_SR(CLIENT_SOCKET) != SOCK_INIT);
		
		ret = connect(CLIENT_SOCKET, serverIP, SERVER_PORT);
		if (ret < 0) {
			printf("connect failed{%d}.Sn_SR()={%d}\n", ret, getSn_SR(CLIENT_SOCKET));
			close(CLIENT_SOCKET);
			osDelay(500);
			continue;
		}
		else {
			printf("connect success...\n");
		}
		
		osSemaphoreRelease(coremark_start_semHandle);
		
		while(1)
		{
				ret = send(CLIENT_SOCKET, (uint8_t*)txBuffer, BUFFERSIZE);
				if (ret < 0) {
					printf("send failed{%d}.\n", ret);
					close(CLIENT_SOCKET); //unexpected close
					break;
				}
				osDelay(100);
		}
  }
  /* USER CODE END 5 */
}

